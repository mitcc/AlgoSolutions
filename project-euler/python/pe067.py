if __name__ == '__main__':
    triangle = []
    f = open('pe067.txt')
    for line in f:
        triangle.append([int(x) for x in line.split(' ')])
    for i in range(len(triangle) - 2, -1, -1):
        for j in range(len(triangle[i])):
            triangle[i][j] += max(triangle[i + 1][j], triangle[i + 1][j + 1])
    print(triangle[0][0])